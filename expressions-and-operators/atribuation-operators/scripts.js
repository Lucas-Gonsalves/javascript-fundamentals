let value;

// Atribuation operators
value = 1;
console.log(value); // ...1

// Icrement
value += 2;
console.log(value); // ...3

// Decrement
value -= 2; // ...1
console.log(value);

// Multiplication
value *= 3;
console.log(3); // ...3

// Dividation
value /= 2;
console.log(value); // ...1.5

// rest of dividation
value %= 2; // ...1.5
console.log(value);

// Power
value **= 2;
console.log(value); // ...2.25
let date = new Date("July 3, 2024 14:30:00")

// year modify
date.setFullYear(2030)

console.log(date) // showing results


// month modify
date.setMonth(7) // June

console.log(date) // showing results


// day modify
date.setDate(10)

console.log(date) // showing results


// hour modify
date.setHours(18)

console.log(date) // showing results


// Minutes modify
date.setMinutes(15)

console.log(date) // showing results


// Seconds modify
date.setSeconds(1)

console.log(date) // showing results
// Template literals (template strings, interpolation of strings)
let username = "Lucas";
let email = "lucas@gmail.com";

// pass more than one parameter
console.log(username, email);

// concatenate a text
let message = "Hello, " + username + ". You have been conected with the email: " + email;

console.log(message);

// template literals
console.log(`Hello, ${username}. You have been conected with the email: ${email}`);
